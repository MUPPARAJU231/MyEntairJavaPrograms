package assignments;

public class NestedIf1
{
	public static void main(String[] args) 
	{
		int a=30;
		int b=70;
		if(a==b)
		{
			System.out.println("1");
		}
		else if(a>=b)
		{
			System.out.println("2");
		}
		else if(a<=b)
		{
			System.out.println("3");
		}
		else
		{
			System.out.println("4");
		}
		
	}
}
