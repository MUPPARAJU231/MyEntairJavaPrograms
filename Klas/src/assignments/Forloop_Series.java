package assignments;

public class Forloop_Series 
{
	public static void main(String[] args)
	{
	//Print 1 to 10
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		System.out.println("===");
	//print 19 to 26
		for(int i=19;i<=26;i++)
		{
			System.out.println(i);
		}
		System.out.println("===");
	//for loop for negative case	
		for(int i=0;i>=-10;i--)
		{
			System.out.println(i);
		}
		
	}
}
