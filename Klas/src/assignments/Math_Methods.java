package assignments;

public class Math_Methods 
{
	public static void main(String[] args)
	{
		System.out.println(Math.PI);
		System.out.println(Math.addExact(5, 40));
		System.out.println(Math.subtractExact(32, 67));
		System.out.println(Math.multiplyExact(3, 23));
		System.out.println(Math.sqrt(625));
		System.out.println(Math.max(98.2, 196.78));
		System.out.println(Math.min(-76, -5));
		System.out.println(Math.random());
	}
}
